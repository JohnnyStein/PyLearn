# Comments
# What are they?
# Comments are lines ignored by the interpreter
# To make a single-line comment in Python
# You just need to put a hashtag before your sentence
# Like this:
# Hello World! ( This is a single line comment )
# All above are single-line comment
# But what if?
# You want a multi-line comment?
# Well, your problem is solved:
# To make a multi line comment in Python
# You can do this:
"""
Hello World!
This is a multi line comment in Python
But the multi-line comment
Is not the better choice
The single-line comments are much better
Trust me!
"""
